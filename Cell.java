
public class Cell {
    private double A;
    private double B;

    public Cell() {
        this.A = 1.0;
        this.B = 0.0;
    }

    public double getA() {
        return A;
    }

    public void setA(double a) {
        A = a;
    }

    public double getB() {
        return B;
    }

    public void setB(double b) {
        B = b;
    }
}